# reproduce_figures.sh — regenerate the entire two-node evaluation dataset
# and every paper figure derived from it, from a clean checkout.
#
# It (1) builds the three SystemC simulators, (2) runs every committed
# experiment driver (run_*.sh) and the self-contained analytical sims
# (sim_*.py), then (3) runs every plot_*.py to rewrite paper/figures/*.pdf.
#
# Each step is independent: a failure is reported but does not abort the
# run, so one missing toolchain (e.g. a stale CSV with no generator) does
# not block the rest. A summary of pass/fail per step is printed at the end.
#
# Env:
#   OPENCLICKNP_ROOT   path to a built OpenClickNP (default ~/OpenClickNP)
#
# Every committed CSV under results/ is regenerated by one of the run_*.sh
# drivers, a sim_*.py, or a self-writing plot (caqm_trace, directory_cliff,
# invalidation_broadcast). See eval/twonode/README.md for the full
# experiment -> data -> figure map.
set -uo pipefail
HERE=$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)
ROOT="$(cd "${HERE}/../.." && pwd)"
cd "${ROOT}"

OK=(); FAIL=()
run() {  # run <label> <command...>
  local label="$1"; shift
  printf '\n\033[1m=== %s ===\033[0m\n' "$label"
  if "$@"; then OK+=("$label"); else FAIL+=("$label"); echo "  [FAILED] $label"; fi
}

# 1. Build the simulators the experiments depend on.
run "build twonode_sim"   bash "${HERE}/build_twonode.sh"
run "build multinode_sim" bash "${HERE}/build_multinode.sh"
run "build conn_setup_sim" bash "${HERE}/build_conn_setup.sh"

# 2. Run every experiment driver. Order is irrelevant; each writes its own CSV.
for s in "${HERE}"/run_*.sh; do
  run "data: $(basename "$s")" bash "$s"
done

# 3. Self-contained analytical sims (no C++ binary; emit figures directly).
for s in sim_cong_control.py sim_conn_setup.py sim_multinode.py; do
  [ -f "${HERE}/$s" ] && run "sim: $s" python3 "${HERE}/$s"
done

# 4. Regenerate every figure.
for p in "${HERE}"/plot_*.py; do
  run "plot: $(basename "$p")" python3 "$p"
done

# 5. Summary.
printf '\n\033[1m========== SUMMARY ==========\033[0m\n'
printf 'OK   : %d\n' "${#OK[@]}"
printf 'FAIL : %d\n' "${#FAIL[@]}"
if [ "${#FAIL[@]}" -gt 0 ]; then
  printf '\nFailed steps:\n'; printf '  - %s\n' "${FAIL[@]:-}"
  exit 1
fi
echo "All evaluation data and figures regenerated."
